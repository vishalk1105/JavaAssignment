class Pattern11{

public static void main(String args[]){


int n=12;
	for(int i=n/2;i>0;i--){
	char ch='A';
	for(int j=0;j<i;j++){
		System.out.print(ch+" ");
		ch++;
	}
	System.out.println();
}
for(int i=n/2;i>0;i--){
	char ch='A';
	for( int j=0;j<=(n/2-i);j++){
		System.out.print(ch+" ");
		ch++;
	}
	System.out.println();
	}
	}
	}